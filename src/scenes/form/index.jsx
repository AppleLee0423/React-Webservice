const phoneRegExp =
  /^((\+[1-9]{1,4}[ -]?)|(\([0-9]{2,3}\)[ -]?)|([0-9]{2,4})[ -]?)*?[0-9]{3,4}[ -]?[0-9]{3,4}$/;

const checkoutSchema = yup.object().shape({
  firstName: yup.string().required("성을 입력해주세요"),
  lastName: yup.string().required("이름을 입력해주세요"),
  email: yup.string().email("올바른 이메일 양식이 아닙니다.").required("이메일을 입력해주세요"),
  contact: yup
    .string()
    .matches(phoneRegExp, "올바른 전화번호 양식이 아닙니다.")
    .required("required"),
  address1: yup.string().required("주소를 입력해주세요."),
  address2: yup.string().required("상세주소를 입력해주세요."),
});
const initialValues = {
  firstName: "",
  lastName: "",
  email: "",
  contact: "",
  address1: "",
  address2: "",
};

